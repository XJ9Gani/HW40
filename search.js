const myInput = document.getElementById("search");
const outputResult = document.querySelector(".result");

function renderData(data) {
  data.map((el) => {
    let block = document.createElement("div");
    let title = document.createElement("h1");
    let description = document.createElement("h3");
    let cost = document.createElement("h4");

    title.innerText = el.title;
    description.innerText = el.description;
    cost.innerText = el.cost;
    title.classList.add("block--title");
    description.classList.add("block--description");
    cost.classList.add("block--cost");
    block.classList.add("block");
    block.append(title, description, cost);
    outputResult.append(block);
  });
}

renderData(arrObj); /// Рендеринг всех данных по дефолту

function filterData(query) {
  outputResult.innerHTML = "";
  const results = arrObj.filter((item) =>
    item.title.toLowerCase().includes(query.toLowerCase())
  );
  renderData(results);
}
myInput.addEventListener("input", (event) => {
  setTimeout(() => {
    const query = event.target.value;
    filterData(query);
  }, 2000);
});
